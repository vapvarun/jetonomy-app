#!/usr/bin/env node
/**
 * inject-branding.js — consumes the Jetonomy App Builder §0 bridge contract and
 * rewrites this Expo app's branding before an EAS build.
 *
 *   node scripts/inject-branding.js customer-config.json
 *
 * It is the consumer half of the contract produced by the Laravel
 * BuildConfigBuilder (GET /api/app/build-config/{customer}). Both sides must
 * agree on the exact shape — see app-server docs/micro-plan.md §0/§8.
 *
 * Behaviour:
 *   1. Parse the config JSON (argv[2]).
 *   2. Mutate app.json `expo` (name, slug, version, scheme, ids, primaryColor).
 *   3. Download non-null assets into ./assets and repoint expo fields at them.
 *      A null asset is a no-op (keep the repo default) — never a failure.
 *   4. Seed the theme:
 *        - update `DEFAULT_ACCENT` in theme/colors.ts (the single seed the app
 *          derives its whole palette from via buildTheme()).
 *        - (re)generate theme/branding.ts with the runtime constants the app
 *          reads: ACCENT, DARK_MODE_DEFAULT, SITE_URL, SITE_URL_HARDCODED.
 *   5. Write app.json back as 2-space pretty JSON. Idempotent / re-runnable.
 */

'use strict';

const fs = require('fs');
const path = require('path');

const ROOT = process.env.INJECT_ROOT
  ? path.resolve(process.env.INJECT_ROOT)
  : path.resolve(__dirname, '..');

const APP_JSON = path.join(ROOT, 'app.json');
const COLORS_TS = path.join(ROOT, 'theme', 'colors.ts');
const BRANDING_TS = path.join(ROOT, 'theme', 'branding.ts');
const ASSETS_DIR = path.join(ROOT, 'assets');

function fail(msg) {
  console.error(`inject-branding: ${msg}`);
  process.exit(1);
}

function ensure(obj, key) {
  if (typeof obj[key] !== 'object' || obj[key] === null) obj[key] = {};
  return obj[key];
}

async function downloadTo(url, destAbs) {
  // Returns true if downloaded, false if skipped (kept default).
  if (!url) return false;
  try {
    const res = await fetch(url);
    if (!res.ok) throw new Error(`HTTP ${res.status}`);
    const buf = Buffer.from(await res.arrayBuffer());
    fs.mkdirSync(path.dirname(destAbs), { recursive: true });
    fs.writeFileSync(destAbs, buf);
    return true;
  } catch (e) {
    // Contract: a failing/missing asset must not break the build.
    console.warn(`inject-branding: asset ${url} skipped (${e.message}); keeping repo default.`);
    return false;
  }
}

function updateDefaultAccent(accent) {
  if (!fs.existsSync(COLORS_TS)) {
    console.warn('inject-branding: theme/colors.ts not found; skipping accent seed.');
    return;
  }
  let src = fs.readFileSync(COLORS_TS, 'utf8');
  const re = /export const DEFAULT_ACCENT\s*=\s*['"][^'"]*['"];/;
  const line = `export const DEFAULT_ACCENT = '${accent}';`;
  if (re.test(src)) {
    src = src.replace(re, line);
  } else {
    src = `${line}\n${src}`;
  }
  fs.writeFileSync(COLORS_TS, src);
}

function writeBranding(cfg) {
  const accent = cfg.theme.accent_color;
  const darkDefault = Boolean(cfg.theme.dark_mode_default);
  const siteUrl = cfg.site.url || '';
  const hardcoded = Boolean(cfg.site.hardcoded);

  const out = `// theme/branding.ts — AUTO-GENERATED by scripts/inject-branding.js.
// Do not edit by hand; values come from the App Builder build-config contract.
// Re-running the injector overwrites this file.

export const ACCENT = '${accent}';
export const DARK_MODE_DEFAULT = ${darkDefault};
export const SITE_URL = '${siteUrl}';
export const SITE_URL_HARDCODED = ${hardcoded};

export const BRANDING = {
  accent: ACCENT,
  darkModeDefault: DARK_MODE_DEFAULT,
  siteUrl: SITE_URL,
  siteUrlHardcoded: SITE_URL_HARDCODED,
} as const;
`;
  fs.mkdirSync(path.dirname(BRANDING_TS), { recursive: true });
  fs.writeFileSync(BRANDING_TS, out);
}

async function main() {
  const cfgPath = process.argv[2];
  if (!cfgPath) fail('usage: node scripts/inject-branding.js <config.json>');
  if (!fs.existsSync(cfgPath)) fail(`config not found: ${cfgPath}`);
  if (!fs.existsSync(APP_JSON)) fail(`app.json not found at ${APP_JSON}`);

  let cfg;
  try {
    cfg = JSON.parse(fs.readFileSync(cfgPath, 'utf8'));
  } catch (e) {
    fail(`invalid JSON config: ${e.message}`);
  }

  if (!cfg.app || !cfg.theme || !cfg.assets || !cfg.site) {
    fail('config missing required sections (app/theme/assets/site)');
  }
  if ((cfg.schema_version || 1) !== 1) {
    fail(`unsupported schema_version ${cfg.schema_version} (this injector supports 1)`);
  }

  const appJson = JSON.parse(fs.readFileSync(APP_JSON, 'utf8'));
  const expo = ensure(appJson, 'expo');

  // 2. Core identity.
  expo.name = cfg.app.name;
  expo.slug = cfg.app.slug;
  expo.version = cfg.app.version;
  expo.scheme = cfg.app.scheme;
  ensure(expo, 'ios').bundleIdentifier = cfg.app.bundle_id;
  ensure(expo, 'android').package = cfg.app.package;
  expo.primaryColor = cfg.theme.accent_color;

  // 3. Assets — download non-null, repoint fields; null = keep default.
  if (await downloadTo(cfg.assets.icon, path.join(ASSETS_DIR, 'icon.png'))) {
    expo.icon = './assets/icon.png';
  }
  if (await downloadTo(cfg.assets.splash, path.join(ASSETS_DIR, 'splash.png'))) {
    const splash = ensure(expo, 'splash');
    splash.image = './assets/splash.png';
    splash.backgroundColor = splash.backgroundColor || cfg.theme.accent_color || '#ffffff';
  }
  const adaptiveUrl = cfg.assets.adaptive_icon || cfg.assets.icon;
  if (await downloadTo(adaptiveUrl, path.join(ASSETS_DIR, 'adaptive-icon.png'))) {
    ensure(ensure(expo, 'android'), 'adaptiveIcon').foregroundImage =
      './assets/adaptive-icon.png';
  }
  // login_bg is consumed by the login screen component, not app.json.
  await downloadTo(cfg.assets.login_bg, path.join(ASSETS_DIR, 'login-bg.png'));

  // 4. Theme seed.
  updateDefaultAccent(cfg.theme.accent_color);
  writeBranding(cfg);

  // 5. Persist app.json (2-space pretty, trailing newline).
  fs.writeFileSync(APP_JSON, JSON.stringify(appJson, null, 2) + '\n');

  console.log(
    `inject-branding: applied "${cfg.app.name}" (accent ${cfg.theme.accent_color}, ` +
      `bundle ${cfg.app.bundle_id}) for customer ${cfg.customer_id}.`
  );
}

main().catch((e) => fail(e.stack || e.message));
